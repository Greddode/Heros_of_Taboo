static void DecodeText(char* out, const unsigned char* data, int len) {
    for (int i = 0; i < len; i++) out[i] = (char)(data[i] ^ XOR_KEY);
    out[len] = '\0';
}

void RenderTextScreen(const unsigned char* data, int len, const char* backHint) {
    int sw = GetScreenWidth();
    int sh = GetScreenHeight();

    ClearBackground((Color){ 15, 15, 25, 255 });

    char* buf = (char*)RL_MALLOC(len + 1);
    if (!buf) return;
    DecodeText(buf, data, len);

    int lineH = 22;
    int y = sh / 6;
    char* line = buf;
    char* next = NULL;
    while (line && *line) {
        next = strchr(line, '\n');
        if (next) *next = '\0';
        int textW = MeasureText(line, 18);
        DrawText(line, (sw - textW) / 2, y, 18, LIGHTGRAY);
        y += lineH;
        if (next) {
            *next = '\n';
            line = next + 1;
        } else {
            break;
        }
    }

    RL_FREE(buf);

    if (backHint) {
        int hintW = MeasureText(backHint, 16);
        DrawText(backHint, (sw - hintW) / 2, sh - 40, 16, (Color){ 100, 100, 130, 255 });
    }
}
